<p>В таблицу `sys_sections` добавляем имя раздела, в котором будет "располагаться" новый модуль - comments. Получаем id = 8.</p>
<p>И теперь через таблицу `sys_classes_sections` связываем сущности нового модуля и раздел. Добавляем 2 записи с `section_id` = 8 и `class_id` = 10 и 11 соответственно. Этим записям будут присвоены id = 10 (для comments) и 11 (для commentsFolder). Совпадение случайное - у вас они могут быть другими.</p>