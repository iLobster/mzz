<p>Затем зарегистрируем этот модуль в приложении.</p>
<p>Из всех определённых нами необходимых экшнов в системе уже зарегистрированы 3: list, edit, delete. Соответственно в таблицу `sys_actions` добавляем ещё одну запись со значением post. Для удобства выпишем идентификаторы всех используемых далее экшнов:</p>
<ul>
    <li>list - 5</li>
    <li>edit - 1</li>
    <li>delete - 2</li>
    <li>post - 19</li>
    <li>editACL - 9 (это служебное действие, оно должно быть у каждого объекта в приложении, построенном на mzz)</li>
</ul>
<<note>>Естественно идентификаторы ваших экшнов (и последующие идентификаторы) могут отличаться от вышеприведённых.<</note>>
<p>В таблицу `sys_modules` также добавляем одну запись - comments, которой присваивается id = 8.</p>
<p>В таблице `sys_classes` хранятся все сущности приложения. Добавляем в неё 2 записи - comments и commentsFolder. В качестве `module_id` для обоих записей указываем 8 (идентификатор модуля). Для вновь созданных записей значения id составили 10 и 11 соответственно.</p>
<p>Теперь мы должны указать какие действия каким сущностям должны соответствовать. Для comments это будут edit, delete, editACL, для commentsFolder - list, post, editACL. Поэтому в таблицу `sys_classes_actions` добавляем записи, связывающие конкретную сущность и соответствующие им экшны. В моём случае это будут следующие записи:</p>
