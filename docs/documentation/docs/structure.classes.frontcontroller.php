<p>Задачей <code>frontController</code> является определение имени необходимого активного шаблона на основании запроса пользователя.
Результат его работы - имя активного шаблона.<br />
<code>frontController</code> запускается в <code>contentFilter</code>, являющимся одним из элементов цепочки фильтров (filter chains).
</p>
<<note>>Примеры его работы доступны в модульных тестах.<</note>>