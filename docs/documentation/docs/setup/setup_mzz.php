<p>
	Для установки MZZ на сервер необходимо скачать его и распаковать в любую директорию, 
	доступную для чтения веб-сервером. Далее вашему приложению необходимо указать этот путь 
	в качестве системной директории (константа SYSTEM_PATH, см.далее). Нет необходимости настраивать
	доступ к этой директории через веб (это необходимо только для запуска тестов и чтения поставляемой
	документации).
</p>