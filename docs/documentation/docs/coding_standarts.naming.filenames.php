<p>Имена файлов, как и имена папок, могут состоять только из латинских букв верхнего или нижнего регистра, чисел или знаков подчеркивания ("_"). Содержащие PHP-код файлы должны иметь расширение ".php".</p>
<p>Примеры правильных имен:</p>
<<code>>
core.php
modules/news/controller/newsEditController.php
<</code>>
<p>Если PHP-файл содержит класс, то его имя должно совпадать с именем этого класса.</p>