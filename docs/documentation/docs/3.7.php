        <li>
                <em>has</em> - опция, аналогичная owns, с тем лишь различием, что в текущей таблице для связи используется другое поле, например Primary Key.<br />
                Пример:
                <<code>>
                        [related_id]<br />
                        accessor = "getRelated"<br />
                        mutator = "setRelated"<br />
