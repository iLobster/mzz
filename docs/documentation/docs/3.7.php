                <p>Пример клиентского кода абсолютно аналогичен примеру из owns.</p>
        </li>
        <li>
                <em>section</em> - данное свойство используется совместно с owns и has для того, чтобы указать, в контексте какой секции должен создаваться присоединяемый DO.<br />
