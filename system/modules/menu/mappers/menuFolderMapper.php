fileLoader::load('menu/menuFolder');

/**
 * menuFolderMapper: маппер
 *
 * @package modules
 * @subpackage menu
 * @version 0.1
 */

class menuFolderMapper extends simpleMapper
{
    /**
     * Имя модуля
     *
     * @var string
     */
    protected $name = 'menu';

    /**
     * Имя класса DataObject
     *
     * @var string
     */
    protected $className = 'menuFolder';

    public function getFolder()
    {
        $folder = $this->create();
        $folder->import(array('obj_id' => $this->getObjId()));
        return $folder;
    }

    private function getObjId()
    { 
        $obj_id = systemToolkit::getInstance()->getObjectId($this->section . '_menuFolder');
        $this->register($obj_id);
        return $obj_id; 
    }

    public function convertArgsToObj($args)
    {
        $obj = $this->create();
        $obj->import(array('obj_id' => $this->getObjId()));
        return $obj;
    }
}

